def generated_nav(nav: list[Any]) -> list[Any]:
    generated = []
    for item in nav:
        key = nav_key(item)
        if key in NAV_FACTORIES:
            generated.append(NAV_FACTORIES[key]())
        else:
            generated.append(item)
    return generated


def nav_key(item: Any) -> str | None:
    if isinstance(item, dict) and len(item) == 1:
        return next(iter(item))
    return None


def data_nav() -> dict[str, list[NavItem]]:
    return {"data": ["data/index.md", {"Dataset": "data/dataset.md"}]}


def api_nav() -> dict[str, list[NavItem]]:
    return python_module_nav("api", API_README.parent, "multimolecule.api")


def datasets_nav() -> dict[str, list[NavItem]]:
    readmes = {readme.parent.name: readme for readme in dataset_readmes()}
    entries = {
        slug: {dataset_label(readme): f"datasets/{slug}.md"}
        for slug, readme in sorted(readmes.items(), key=lambda item: sort_key(dataset_label(item[1])))
    }
    metadata = {slug: frontmatter(readme) for slug, readme in readmes.items()}
    groups: dict[str, dict[str, list[NavItem]]] = defaultdict(lambda: defaultdict(list))
    for slug, entry in entries.items():
        language, group = dataset_group(metadata[slug])
        groups[language][group].append(entry)

    nav: list[NavItem] = ["datasets/index.md"]
    for language in sorted(groups, key=sort_key):
        nav.append({language: [{group: groups[language][group]} for group in sorted(groups[language], key=sort_key)]})
    return {"datasets": nav}


def io_nav() -> dict[str, list[NavItem]]:
    return python_module_nav("io", IO_README.parent, "multimolecule.io")


def modules_nav() -> dict[str, list[NavItem]]:
    nav: list[NavItem] = ["modules/index.md"]
    for label, slug in sorted(
        readme_relative_links(MODULE_README_ROOT / "README.md"), key=lambda item: sort_key(item[0])
    ):
        if (MODULE_README_ROOT / slug / "README.md").exists() or public_object_paths(f"multimolecule.modules.{slug}"):
            nav.append({label: f"modules/{slug}.md"})
    return {"modules": nav}


def runner_nav() -> dict[str, list[NavItem]]:
    return python_module_nav("runner", RUNNER_README.parent, "multimolecule.runner")


def tokenisers_nav() -> dict[str, list[NavItem]]:
    return {
        "tokenisers": [
            "tokenisers/index.md",
            *({tokeniser_label(readme): f"tokenisers/{slug}.md"} for slug, readme in tokeniser_readmes()),
        ]
    }


def utils_nav() -> dict[str, list[NavItem]]:
    return {"utils": [{title_from_slug(slug): f"utils/{slug}.md"} for slug, _object_paths in util_entries()]}


def models_nav() -> dict[str, list[NavItem]]:
    languages: dict[str, list[tuple[str, dict[str, str]]]] = defaultdict(list)
    for readme in MODEL_README_ROOT.glob("*/README.md"):
        metadata = frontmatter(readme)
        title, _body = split_readme(readme)
        label = str(metadata.get("pretty_name") or title)
        language = title_from_slug(str(metadata.get("language", "other")))
        languages[language].append((sort_key(label), {label: f"models/{readme.parent.name}.md"}))

    nav: list[NavItem] = ["models/index.md"]
    for language in sorted(languages, key=sort_key):
        entries = [entry for _sort_key, entry in sorted(languages[language], key=lambda item: item[0])]
        nav.append({language: entries})
    nav.append(
        {
            "utils": [
                {"configuration_utils": "models/configuration_utils.md"},
                {"modeling_outputs": "models/modeling_outputs.md"},
            ]
        }
    )
    return {"models": nav}


def pipelines_nav() -> dict[str, list[NavItem]]:
    nav: list[NavItem] = ["pipelines/index.md"]
    nav.extend({title: f"pipelines/{slug}.md"} for slug, title, _object_path in pipeline_entries())
    return {"pipelines": nav}


NAV_FACTORIES: dict[str, NavFactory] = {
    "api": api_nav,
    "data": data_nav,
    "datasets": datasets_nav,
    "io": io_nav,
    "modules": modules_nav,
    "models": models_nav,
    "pipelines": pipelines_nav,
    "runner": runner_nav,
    "tokenisers": tokenisers_nav,
    "utils": utils_nav,
}


# ---------------------------------------------------------------------------
# MkDocs hook lifecycle
# ---------------------------------------------------------------------------


def write_generated(docs_dir: Path, path: str, content: str) -> None:
    output = docs_dir / path
    if output.exists() and not output.read_text().startswith(GENERATED_MARKER):
        return
    output.parent.mkdir(parents=True, exist_ok=True)
    output.write_text(f"{GENERATED_MARKER}\n\n{content.rstrip()}\n")


def remove_generated(docs_dir: Path, path: str) -> None:
    output = docs_dir / path
    if output.exists() and output.read_text().startswith(GENERATED_MARKER):
        output.unlink()


def cleanup_generated(docs_dir: Path) -> None:
    for path, _content in generated_pages():
        remove_generated(docs_dir, path)


def on_config(config):
    global GENERATED_DOCS_DIR

    docs_dir = Path(config["docs_dir"])
    GENERATED_DOCS_DIR = docs_dir
    for path, content in generated_pages():
        write_generated(docs_dir, path, content)
    if config.get("nav"):
        config["nav"] = generated_nav(config["nav"])
    return config


def on_post_build(config):
    cleanup_generated(Path(config["docs_dir"]))


def on_build_error(error):
    if GENERATED_DOCS_DIR is not None:
        cleanup_generated(GENERATED_DOCS_DIR)
