ACRONYM_WORDS = {
    "dna": "DNA",
    "rna": "RNA",
}

AUTODOC_DISABLED_VALUES = {"0", "false", "no", "off"}

RELATIVE_DATASET_LINK = re.compile(r"(\[[^\]]+\]\()(\.?\.?/[^)#?]+)([#?][^)]*)?(\))")
README_LINK = re.compile(r"\[([^\]]+)\]\(([^):#]+)\)")

Page = tuple[str, str]
