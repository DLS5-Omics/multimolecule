from __future__ import annotations

import importlib
import inspect
import re
from collections import defaultdict
from collections.abc import Callable
from io import StringIO
from pathlib import Path
from typing import Any

import chanfig

ROOT = Path(__file__).resolve().parents[1]
PACKAGE_ROOT = ROOT / "multimolecule"

DATA_README = PACKAGE_ROOT / "data" / "README.md"
DATASET_README_ROOT = PACKAGE_ROOT / "datasets"
IO_README = PACKAGE_ROOT / "io" / "README.md"
API_README = PACKAGE_ROOT / "api" / "README.md"
MODEL_README_ROOT = PACKAGE_ROOT / "models"
MODULE_README_ROOT = PACKAGE_ROOT / "modules"
RUNNER_README = PACKAGE_ROOT / "runner" / "README.md"
TOKENISER_README_ROOT = PACKAGE_ROOT / "tokenisers"
UTILS_ROOT = PACKAGE_ROOT / "utils"

GENERATED_MARKER = "<!-- This file is generated by docs/gen_pages.py. Do not edit directly. -->"
GENERATED_DOCS_DIR: Path | None = None

ACRONYM_WORDS = {
    "dna": "DNA",
    "rna": "RNA",
}

RELATIVE_DATASET_LINK = re.compile(r"(\[[^\]]+\]\()(\.?\.?/[^)#?]+)([#?][^)]*)?(\))")
README_LINK = re.compile(r"\[([^\]]+)\]\(([^):#]+)\)")

Page = tuple[str, str]
NavItem = str | dict[str, Any]
PageFactory = Callable[[], list[Page]]
NavFactory = Callable[[], dict[str, Any]]


