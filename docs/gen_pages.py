def generated_data_pages() -> list[Page]:
    pages: list[Page] = []
    if DATA_README.exists():
        pages.append(("data/index.md", readme_page(DATA_README)))
    if not autodoc_enabled():
        return pages

    if object_exists("multimolecule.data.Dataset"):
        pages.append(("data/dataset.md", api_page("Dataset", ["multimolecule.data.Dataset"])))
    return pages


def generated_dataset_pages() -> list[Page]:
    pages: list[Page] = []
    datasets_readme = DATASET_README_ROOT / "README.md"
    if datasets_readme.exists():
        pages.append(("datasets/index.md", readme_page(datasets_readme)))
    pages.extend(
        (
            f"datasets/{readme.parent.name}.md",
            readme_page(readme, rewrite_dataset_links=True),
        )
        for readme in dataset_readmes()
    )
    return pages


def generated_io_pages() -> list[Page]:
    return python_module_pages("io", IO_README.parent, "multimolecule.io")


def generated_api_pages() -> list[Page]:
    return python_module_pages("api", API_README.parent, "multimolecule.api")


def generated_module_pages() -> list[Page]:
    pages: list[Page] = []
    modules_readme = MODULE_README_ROOT / "README.md"
    if modules_readme.exists():
        pages.append(("modules/index.md", readme_page(modules_readme)))

    for label, slug in readme_relative_links(modules_readme):
        readme = MODULE_README_ROOT / slug / "README.md"
        body = split_readme(readme)[1] if readme.exists() else ""
        if not autodoc_enabled():
            if body:
                pages.append((f"modules/{slug}.md", join_sections(f"# {label}", body)))
            continue
        object_paths = public_object_paths(f"multimolecule.modules.{slug}")
        if body or object_paths:
            pages.append((f"modules/{slug}.md", api_page(label, object_paths, body)))
    return pages


def generated_runner_pages() -> list[Page]:
    return python_module_pages("runner", RUNNER_README.parent, "multimolecule.runner")


def generated_tokeniser_pages() -> list[Page]:
    pages: list[Page] = []
    tokenisers_readme = TOKENISER_README_ROOT / "README.md"
    if tokenisers_readme.exists():
        pages.append(("tokenisers/index.md", readme_page(tokenisers_readme)))

    for slug, readme in tokeniser_readmes():
        title, body = split_readme(readme)
        alphabet = TOKENISER_README_ROOT / slug / "ALPHABET.md"
        alphabet_body = split_readme(alphabet)[1] if alphabet.exists() else ""
        object_path = f"multimolecule.tokenisers.{title}"
        pages.append(
            (
                f"tokenisers/{slug}.md",
                tokeniser_page(title, body, object_path, alphabet_body),
            )
        )
    return pages


def generated_util_pages() -> list[Page]:
    return [
        (f"utils/{slug}.md", api_page(title_from_slug(slug), object_paths)) for slug, object_paths in util_entries()
    ]


def generated_model_pages() -> list[Page]:
    pages = []
    for model, readme in model_entries():
        title, body = split_readme(readme)
        pages.append((f"models/{model}.md", model_page(model, title, body)))
    return pages


def generated_pipeline_pages() -> list[Page]:
    return [
        (f"pipelines/{slug}.md", pipeline_page(title, object_path)) for slug, title, object_path in pipeline_entries()
    ]


PAGE_FACTORIES: tuple[PageFactory, ...] = (
    generated_data_pages,
    generated_dataset_pages,
    generated_io_pages,
    generated_api_pages,
    generated_module_pages,
    generated_runner_pages,
    generated_tokeniser_pages,
    generated_util_pages,
    generated_model_pages,
    generated_pipeline_pages,
)


def generated_pages() -> list[Page]:
    return [page for factory in PAGE_FACTORIES for page in factory()]


