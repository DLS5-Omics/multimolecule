def object_exists(object_path: str) -> bool:
    module_path, _, object_name = object_path.rpartition(".")
    try:
        module = importlib.import_module(module_path)
    except Exception:
        return False
    return hasattr(module, object_name)


def public_object_paths(module_path: str) -> list[str]:
    try:
        module = importlib.import_module(module_path)
    except Exception:
        return []

    names = getattr(module, "__all__", None)
    if names is not None:
        return [f"{module_path}.{name}" for name in names if hasattr(module, name)]

    object_paths = []
    for name, obj in vars(module).items():
        if name.startswith("_"):
            continue
        if not (inspect.isclass(obj) or inspect.isfunction(obj)):
            continue
        if getattr(obj, "__module__", None) == module.__name__:
            object_paths.append(f"{module_path}.{name}")
    return object_paths


def python_module_pages(section: str, package_root: Path, package: str) -> list[Page]:
    pages: list[Page] = []
    readme = package_root / "README.md"
    if readme.exists():
        pages.append((f"{section}/index.md", readme_page(readme)))

    if not autodoc_enabled():
        pages.extend(
            (
                f"{section}/{module_file.stem}.md",
                f"# {title_from_slug(module_file.stem)}",
            )
            for module_file in python_module_files(package_root)
        )
        return pages

    for module_file in python_module_files(package_root):
        slug = module_file.stem
        object_paths = public_object_paths(f"{package}.{slug}")
        if object_paths:
            pages.append((f"{section}/{slug}.md", api_page(title_from_slug(slug), object_paths)))
    return pages


def python_module_nav(section: str, package_root: Path, package: str) -> dict[str, list[NavItem]]:
    nav: list[NavItem] = []
    if (package_root / "README.md").exists():
        nav.append(f"{section}/index.md")
    if not autodoc_enabled():
        nav.extend(
            {title_from_slug(module_file.stem): f"{section}/{module_file.stem}.md"}
            for module_file in python_module_files(package_root)
        )
        return {section: nav}

    for module_file in python_module_files(package_root):
        slug = module_file.stem
        if public_object_paths(f"{package}.{slug}"):
            nav.append({title_from_slug(slug): f"{section}/{slug}.md"})
    return {section: nav}


def python_module_files(package_root: Path) -> list[Path]:
    return [module_file for module_file in sorted(package_root.glob("*.py")) if module_file.name != "__init__.py"]


def dataset_readmes() -> list[Path]:
    return sorted(DATASET_README_ROOT.glob("*/README.md"))


def model_readmes() -> list[Path]:
    return sorted(MODEL_README_ROOT.glob("*/README.md"))


def git_tracked_paths(pattern: str) -> set[Path] | None:
    try:
        result = subprocess.run(
            ["git", "-C", str(ROOT), "ls-files", "--", pattern],
            capture_output=True,
            check=True,
            text=True,
        )
    except (OSError, subprocess.CalledProcessError):
        return None
    return {ROOT / path for path in result.stdout.splitlines()}


def model_entries() -> list[tuple[str, Path]]:
    tracked_readmes = git_tracked_paths("multimolecule/models/*/README.md")
    tracked_modeling = git_tracked_paths("multimolecule/models/*/modeling_*.py")
    if tracked_readmes is not None and tracked_modeling is not None:
        return [
            (readme.parent.name, readme)
            for readme in sorted(tracked_readmes)
            if readme.parent / f"modeling_{readme.parent.name}.py" in tracked_modeling
        ]

    entries = []
    for readme in model_readmes():
        model = readme.parent.name
        if (readme.parent / f"modeling_{model}.py").exists():
            entries.append((model, readme))
    return entries


def tokeniser_readmes() -> list[tuple[str, Path]]:
    readmes = [
        (readme.parent.name, readme)
        for readme in TOKENISER_README_ROOT.glob("*/README.md")
        if (TOKENISER_README_ROOT / readme.parent.name / "ALPHABET.md").exists()
    ]
    return sorted(readmes, key=lambda item: sort_key(tokeniser_label(item[1])))


def util_entries() -> list[tuple[str, list[str]]]:
    if not autodoc_enabled():
        return []

    entries = []
    for init_file in sorted(UTILS_ROOT.glob("**/__init__.py")):
        if init_file.parent == UTILS_ROOT:
            continue
        relative = init_file.parent.relative_to(UTILS_ROOT)
        slug = relative.as_posix()
        object_paths = public_object_paths(f"multimolecule.utils.{slug.replace('/', '.')}")
        if object_paths:
            entries.append((slug, object_paths))
    return sorted(entries, key=lambda entry: sort_key(title_from_slug(entry[0])))


def pipeline_entries() -> list[tuple[str, str, str]]:
    if not autodoc_enabled():
        return pipeline_entries_from_source()

    try:
        importlib.import_module("multimolecule.pipelines")
        pipelines = importlib.import_module("transformers.pipelines")
    except Exception:
        return []

    entries = []
    for slug, config in pipelines.PIPELINE_REGISTRY.supported_tasks.items():
        pipeline_class = config.get("impl")
        module = getattr(pipeline_class, "__module__", "")
        if not module.startswith("multimolecule.pipelines."):
            continue
        entries.append((slug, title_from_slug(slug), f"{module}.{pipeline_class.__name__}"))
    return sorted(entries, key=lambda entry: sort_key(entry[1]))


def pipeline_entries_from_source() -> list[tuple[str, str, str]]:
    init_file = PACKAGE_ROOT / "pipelines" / "__init__.py"
    tree = ast.parse(init_file.read_text())
    imports: dict[str, str] = {}

    for node in tree.body:
        if not isinstance(node, ast.ImportFrom) or node.level != 1 or node.module is None:
            continue
        for alias in node.names:
            imports[alias.asname or alias.name] = f"multimolecule.pipelines.{node.module}.{alias.name}"

    entries = []
    for node in ast.walk(tree):
        if not isinstance(node, ast.Call):
            continue
        if not isinstance(node.func, ast.Attribute) or node.func.attr != "register_pipeline":
            continue
        if len(node.args) < 2:
            continue
        slug_node, class_node = node.args[:2]
        if not isinstance(slug_node, ast.Constant) or not isinstance(slug_node.value, str):
            continue
        if not isinstance(class_node, ast.Name) or class_node.id not in imports:
            continue
        slug = slug_node.value
        entries.append((slug, title_from_slug(slug), imports[class_node.id]))
    return sorted(entries, key=lambda entry: sort_key(entry[1]))


