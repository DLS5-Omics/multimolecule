def api_page(title: str, object_paths: list[str], body: str = "") -> str:
    if not autodoc_enabled():
        return join_sections(f"# {title}", body)

    api = "\n\n".join(f"::: {object_path}" for object_path in object_paths)
    return join_sections(f"# {title}", body, api)


def model_page(model: str, title: str, body: str) -> str:
    if not autodoc_enabled():
        return join_sections(f"# {title}", body)

    api = "\n".join(
        [
            "## API Reference",
            "",
            f"::: multimolecule.models.{model}",
            "    options:",
            "      show_root_heading: false",
            "      filters:",
            '        - "!.*Tokenizer.*"',
        ]
    )
    return join_sections(f"# {title}", body, api)


def pipeline_page(title: str, object_path: str) -> str:
    if not autodoc_enabled():
        return f"# {title}"

    return "\n".join(
        [
            f"# {title}",
            "",
            f"::: {object_path}",
            "    options:",
            "      show_root_full_path: false",
        ]
    )


def tokeniser_page(title: str, body: str, object_path: str, alphabet_body: str) -> str:
    api = f"::: {object_path}" if autodoc_enabled() and object_exists(object_path) else ""
    return join_sections(f"# {title}", body, api, alphabet_body)


