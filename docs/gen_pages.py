def strip_frontmatter(lines: list[str]) -> list[str]:
    if not lines or lines[0].strip() != "---":
        return lines
    for index, line in enumerate(lines[1:], start=1):
        if line.strip() == "---":
            return lines[index + 1 :]
    return lines


def frontmatter(readme: Path) -> dict[str, Any]:
    lines = readme.read_text().splitlines()
    if not lines or lines[0].strip() != "---":
        return {}

    for index, line in enumerate(lines[1:], start=1):
        if line.strip() == "---":
            metadata = chanfig.load(StringIO("\n".join(lines[1:index])), method="yaml")
            if hasattr(metadata, "dict"):
                metadata = metadata.dict()
            return metadata if isinstance(metadata, dict) else {}
    return {}


def split_readme(readme: Path) -> tuple[str, str]:
    lines = strip_frontmatter(readme.read_text().splitlines())
    for index, line in enumerate(lines):
        if line.startswith("# "):
            title = line[2:].strip()
            body = "\n".join(lines[index + 1 :]).lstrip()
            return title, body
    return readme.parent.name, "\n".join(lines).lstrip()


def readme_page(readme: Path, *, rewrite_dataset_links: bool = False) -> str:
    title, body = split_readme(readme)
    if rewrite_dataset_links:
        body = rewrite_dataset_relative_links(body)
    return join_sections(f"# {title}", body)


def readme_relative_links(readme: Path) -> list[tuple[str, str]]:
    if not readme.exists():
        return []
    links = []
    for label, target in README_LINK.findall(readme.read_text()):
        if "/" in target or "." in target:
            continue
        links.append((label, target))
    return links


def rewrite_dataset_relative_links(body: str) -> str:
    known_datasets = {readme.parent.name for readme in dataset_readmes()}

    def replace(match: re.Match[str]) -> str:
        prefix, target, suffix, close = match.groups()
        suffix = suffix or ""
        dataset = target.removeprefix("./").removeprefix("../").rstrip("/")
        if dataset not in known_datasets:
            return match.group(0)
        return f"{prefix}{dataset}.md{suffix}{close}"

    return RELATIVE_DATASET_LINK.sub(replace, body)


def dataset_label(readme: Path) -> str:
    metadata = frontmatter(readme)
    title, _body = split_readme(readme)
    return str(metadata.get("pretty_name") or title)


def dataset_group(metadata: dict[str, Any]) -> tuple[str, str]:
    tags = metadata.get("tags", [])
    if isinstance(tags, list) and len(tags) >= 2:
        return str(tags[-2]), str(tags[-1])

    language = title_from_slug(str(metadata.get("language", "other")))
    return language, "Other"


def tokeniser_label(readme: Path) -> str:
    title, _body = split_readme(readme)
    return title_from_slug(title.removesuffix("Tokenizer"))


def join_sections(*sections: str) -> str:
    return "\n\n".join(section for section in sections if section)


def sort_key(label: str) -> str:
    return label.casefold()


def title_from_slug(slug: str) -> str:
    words = []
    for word in re.split(r"[-_/]+", slug):
        if not word:
            continue
        key = word.casefold()
        if key in ACRONYM_WORDS:
            words.append(ACRONYM_WORDS[key])
        elif any(character.isupper() for character in word[1:]):
            words.append(word)
        else:
            words.append(word.capitalize())
    return " ".join(words)


