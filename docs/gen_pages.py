def model_family_count() -> int:
    return len(model_entries())


def dataset_count() -> int:
    return len(
        [
            dataset_dir
            for dataset_dir in sorted(DATASET_README_ROOT.iterdir())
            if dataset_dir.is_dir() and (dataset_dir / f"{dataset_dir.name}.py").exists()
        ]
    )


def pipeline_count() -> int:
    return len(pipeline_entries())


def homepage_stats(*, zh: bool = False) -> list[dict[str, str]]:
    labels = ("模型家族", "数据集", "任务 pipeline") if zh else ("model families", "datasets", "task pipelines")
    values = (model_family_count(), dataset_count(), pipeline_count())
    return [{"value": str(value), "label": label} for value, label in zip(values, labels, strict=True)]


